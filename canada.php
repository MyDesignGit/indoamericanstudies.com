                                      <td colspan="2" valign="top" class="style1"><p align="justify"> Canada is as varied as it is vast, stretching over 7,000km from the   Atlantic to the Pacific. 90% of the country's 34 million inhabitants   live less than 100 miles of the US border in the south leaving the   majority of the 10,000,000 square kilometers of wilderness untouched. </p>
                                        <p align="justify"> Canada is made up of 10 provinces and 3 territories in the north. The   Yukon Territory, Northwest Territory and Nunavut make up Canada's three   territories which umbrella the provinces in the south. The majority of   the territories remain uninhabited and air transport is often the only   way around. </p>
                                        <p align="justify"><strong> History </strong><br/>
                                          The first inhabitants of Canada were native Indian peoples, primarily   the Inuit (Eskimo). The Norse explorer Leif Eriksson probably reached   the shores of Canada (Labrador or Nova Scotia) in 1000, but the history   of the white man in the country actually began in 1497, when John Cabot,   an Italian in the service of Henry VII of England, reached Newfoundland   or Nova Scotia. Canada was taken for France in 1534 by Jacques Cartier. </p>
                                        <p align="justify"> The actual settlement of New France, as it was then called, began in   1604 at Port Royal in what is now Nova Scotia; in 1608, Qu&eacute;bec was   founded. France's colonization efforts were not very successful, but   French explorers by the end of the 17th century had penetrated beyond   the Great Lakes to the western prairies and south along the Mississippi   to the Gulf of Mexico. </p>
                                        <p align="justify"> Meanwhile, the English Hudson's Bay Company had been established in   1670. Because of the valuable fisheries and fur trade, a conflict   developed between the French and English; in 1713, Newfoundland, Hudson   Bay, and Nova Scotia (Acadia) were lost to England. </p>
                                        <p align="justify"> During the Seven Years' War (1756&ndash;1763), England extended its conquest,   and the British general James Wolfe won his famous victory over Gen.   Louis Montcalm outside Qu&eacute;bec on Sept. 13, 1759. The Treaty of Paris in   1763 gave England control. </p>
                                        <p align="justify"> Later in 1776, the year of American Independence, colonists loyal to the British Empire fled USA and settled in Canada. </p>
                                        <p align="justify"><strong> Economy </strong><br/>
                                          Canada is one of the world's wealthiest nations, with a high per-capita   income, and it is a member of the Organization for Economic Co-operation   and Development (OECD) and the G8. </p>
                                        <p align="justify"> It is one of the world's top ten trading nations. Canada is a mixed   market, ranking above the U.S. on the Heritage Foundation's index of   economic freedom and higher than most western European nations. </p>
                                        <p align="justify"> As of October 2009, Canada's national unemployment rate was 8.6%.   Provincial unemployment rates vary from a low of 5.8% in Manitoba to a   high of 17% in Newfoundland and Labrador. </p>
                                        <p align="justify"> Canada is one of the few developed nations that are net exporters of   energy. Canada is one of the world's largest suppliers of agricultural   products. It is the largest producer of zinc and uranium, and is a   global source of many other natural resources, such as gold, nickel,   aluminium, and lead. </p>
                                        <p align="justify"> Canada also has a sizable manufacturing sector centered in southern   Ontario and Qu&eacute;bec, with automobiles and aeronautics representing   particularly important industries. </p>
                                        <p align="justify"><strong> Climate</strong><br/>
                                          In Canada, there are four different seasons: spring, summer, autumn   (fall), and winter. In the winter, the days are shorter and colder. In   the summer, the days are longer and warmer. Overall, the climate varies   dramatically across Canada. Many factors influence climate, such as   distance from large bodies of water, latitude, elevation, and prevailing   winds. </p>
                                        <p align="justify"> Some regions, particularly the southern coastal regions, have relatively   mild climates. Temperatures might range from -10&deg; to 5&deg; Celsius in the   winter and 10&deg;C to 30&deg;C in the summer. In these coastal regions, there   is more rain than snow during the winter. Some parts of Canada, such as   the West Coast, are quite humid. Other parts, like the Prairies, are   very dry. </p>
                                        <p align="justify"><strong> Culture</strong><br/>
                                          Canadian culture has historically been influenced by British, French,   and Aboriginal cultures and traditions. Many Canadians value   multiculturalism and see Canada as being inherently multicultural. The   country's culture has been heavily influenced by American culture   because of its proximity and the high rate of migration between the two   countries. The great majority of English-speaking immigrants to Canada   between 1755 and 1815 were Americans from the Thirteen Colonies; during   and immediately after the War of Independence (46,000 Americans loyal to   the British crown came to Canada). </p>
                                        <p align="justify"> American media and entertainment are popular, if not dominant, in   English Canada; conversely, many Canadian cultural products and   entertainers are successful in the United States and worldwide. Many   cultural products are marketed toward a unified &quot;North American&quot; or   global market. </p>
                                        <p align="justify"><strong> Food</strong><br/>
                                          The favorite foods of Canadians vary slightly from region to region, and   are strongly influenced by their family heritage, especially in   relation to holiday celebrations. Along the Atlantic coast, seafood and   dishes derived from English traditions (except in Qu&eacute;bec) are common. In   Qu&eacute;bec, favorite foods come from the area's French heritage. Throughout   Canada, maple syrup and maple products are popular, reflecting the   significance of the maple tree, whose leaf adorns the flag of Canada. </p>
