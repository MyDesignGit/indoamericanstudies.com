                                        <strong>Employment                                        </strong>
                                        <p align="justify">All non-European students are allowed to work for 20 hours part-time per  week, during college days and full-time during vacations, for the permitted  duration of stay in Ireland. <br/>
                                      However, this is applicable only for Full-time students and not for  foundation-level program students. This new regulation has come into effect  from 18th April 2005. </p></td>
