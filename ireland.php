                                      <td colspan="2" valign="top" class="style1">
                                        <p align="justify">Ireland  is the land of scholars and saints. When we consider greats like Jonathan  Swift, George Bernard Shaw, James Joyce, Oscar Wilde, Yeats etc., we know for a  fact why this 'Celtic Tiger' is called so! <br/>
                                          <br/>
                                          As far as education for International students is concerned, Ireland is an  emerging country, which is now spreading its wings. With students seeking new frontiers for higher education, Ireland has  emerged as a very promising country because of two strengths - the medium of  instruction and cheaper costs.<br/>
                                          <br/> 
                                          More attractive for students is the visa, which is easy to obtain than any  other country's (at least popular!) <br/>
                                          One strong reason why Ireland  is also trying to attract more number of students is the fact that their  population is not growing much and they would have a very low level of growth  in further education, in terms of numbers.<br/> 
                                          <br/>
                                          Trinity College  in Dublin is  the oldest and most prestigious university that was founded in 1592. National University  of Ireland, University  College of Dublin, University college Cork, University of Limerick are equally reputed  universities.<br/> 
                                          <br/>
                                          Dublin Institute of Technology is the largest among Institutes of  Technologies in Ireland.  Others include Waterford Institute of Technology, Athlone Institute of Technology,  Letterkenny Institute of Technology etc. </p>
                                        <strong>Education system                                        </strong>
                                        <p align="justify">Irish universities offer program right from the Certificate level to the  Doctoral level, and in almost all fields of study. Engineering and Technology  are two areas which are quite popular among international students. One  surprising fact is that Ireland  is touted to be the largest exporter of IT software, worldwide! <br/>
                                          There are seven universities, 14 technical institutes and 24 recognised  independent institutions in Ireland.  There is a close association between universities and industries in Ireland, which  translates into internships and employment for students after completion of  their education. <br/>
                                          The Irish education system costs of primary education upto class 8 and  secondary education costs of classes 9 to 12. After a 'Senior Cycle' period of  one to two years, students take up further education in universities,  Institutes of Technology or colleges. <br/>
                                          Bachelor's degrees are of three to four years duration. A subsequent  Graduate diploma is of one year duration. Further, a master's level degree is  from one to two years duration. <br/>
                                          Doctoral programs usually takes between 3-5 years or original research. <br/>
                                          Valmiki's gamut of services provide total assistance and guidance about the  education system, studying at all levels in your country of choice and plan  your total process, right from pre-application to visa, in a professional and  result-oriented manner. For more information about our services.                                        </p>
                                        <strong>Admission                                        </strong>
                                        <p align="justify">Admission for Indian students is possible if they have their complete  education in English medium, but with higher scores. You could apply through  Valmiki for admission into most Irish universities, since we are official  agents for them. This makes your work smoother and simpler. <br/>
                                          Academic year starts every year in September and ends by June. Deadlines for  most programs fall between 1st February and 1st May. The academic year is  divided into two or three semesters. </p>
                                        <strong>Required Tests                                        </strong>
                                        <p align="justify">Admission is preferred either into the Bachelor's or Master's level by most  Indian students. For both levels, a good academic background - minimum first  class - and English medium instruction are required. If proficiency in English  is not good, some universities do insist on having scores either from TOEFL or  IELTS. <br/>
                                          GRE is not required for admission into Irish universities. </p>
                                        <strong>Cost of Education                                        </strong>
                                        <p align="justify">At the Bachelor's level, the average cost of engineering education is  between 9,500-13,000 Euros. Business education costs aprox. 10,000-14,000.  Medical education is the costliest, which will setback a student by at least  23,000 Euros per year. <br/>
                                          <br/>
                                          Master's level cost approx. 6,000-12,000 Euros per year. <br/>
                                          <br/>
                                          Additionally accommodation, living expenses, books and other miscellaneous  expenditure would come to approx. 6,000-8,000 Euros, depending upon the  location. <br/>
                                          <br/>
                                          A small number of scholarships for international students are available from  the universities and colleges themselves. These are awarded solely at the  discretion of the individual institutions who set down their own criteria for  eligibility. Students are advised to contact the institution of their choice  directly, to obtain further information. <br/>
                                          The Department of Education and Science also has a limited number of  scholarships. For details contact: </p>
