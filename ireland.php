                                        <p align="justify">Aspects like Education policy, curriculum, national exams etc. are  administered by the Department of Education and Science in Ireland. <br/>
                                          Universities award the degrees/diploms in their own colleges. Whereas,  non-university colleges award the degrees/diplomas issued by the Higher  Education Training Awards Council (HETAC), which administers standards at all  levels of higher education, upto Ph.D level. </p>
