                                          <td valign="top" align="left"><p align="justify"> <span class="style1"><strong>Modern Australia was founded with the influx of European   settlers just over two hundred years ago, but the Aborigines inhabited   the island continent for tens of thousands of years before that.</strong> <br/>
                                          <br/>
                                          They numbered a few hundred thousand before the European influx. But two centuries  of discrimination and expropriation followed, and at one point the indigenous population  fell as low as 60,000. Australia's politicians at first looked to Europe and the  US in foreign policy, but in the past 20 years or so they have made their near neighbors  a priority. </span></p>
                                              <p align="justify"> <span class="style1"><strong>History</strong><br/>
                                              Australia has been colonized for above 42,000 years by aboriginal   Australians, following the periodic visits of fishermen from Europe and   discovery of Dutch voyagers in 1606. Later in 1770, the eastern part of   Australia was claimed by British who transported convicts to   New South   Wales in 1788. Many of the first settlers were thus British convicts.   Free settlers arrived in increasing numbers, particularly after the   discovery of gold in the mid-19th century. </span></p>
                                            <p align="justify" class="style1"> In 19th century, another five colonies were explored. On 1 January 1901,   Australia was formed with the federation of 6 colonies. Since then it   has maintained a constant liberal democratic political system. Canberra   is the capital city located in Australian Capital Territory (ACT )and   population is approx. 21,511,788  as in March 2009. Among them 60% has   concentrated in and around the state capitals of Sydney, Melbourne,   Brisbane, Perth and Adelaide. Today, 99% of the population is of   European or Asian descent.</p>
                                            <p align="justify"> <span class="style1"><strong>Economy</strong><br/>
                                            Australia has a prosperous and mixed economy in western-style with a per   capita GDP in excess of many urbanized countries like, UK, Germany, and   France. It is ranked third in the United Nations in 2007 and sixth in   The Economist in 2005. By the end of March 2008, the GDP per capita of   Australia was about $ 824.9 Billion (<a href="https://www.cia.gov/library/publications/the-world-factbook/geos/as.html" target="_blank">CIA World Factbook</a>).   The inflation rate was 4.7% per annum and 4.5% of unemployment rate in   2008. Service sector constitutes 69% of GDP, whereas agriculture and   natural resources constitute 3% and 5% of GDP respectively. These   statistical facts advocate that there is excellent scope for career   formation in every sector.</span></p>
                                            <p align="justify"> <span class="style1"><strong>Climate</strong><br/>
                                            Australian climate varies widely based on the region, which was significantly influenced  by ocean currents. Largest part of Australia was outback, which is desert or semi-dry.  The southern region has a temperate climate and moderately fertile soil and the  northern region has a tropical climate, which was partly rainforests, grasslands  and deserts. Large number of population lives within the south-eastern coastline  in a temperate atmosphere. All a whole, people from any country can have a pleasant  atmosphere in Australia. </span></p>
                                            <p align="justify"> <span class="style1"><strong>Culture</strong><br/>
  Australia&rsquo;s culture was greatly derived from the European lineage, but there  are some distinctive Australian characteristics grown from the atmosphere, aboriginal  culture and influence of its neighborhood. Since the past 50 years, the culture  of Australia was strongly influenced by the American popular culture, especially  movies and television. Literature, movies, opera, music, dance, painting, theater  and crafts are the dynamism and creative arts of Australia, which has achieved global  appreciation. The most of the immigrants are from Asian and European countries. </span></p>
                                            <p align="justify"> <span class="style1"><strong>Food</strong><br/>
                                            The traditional cuisine of Australia was rooted from the European   gastronomy, which was bought from the early settlers. It usually   consists of pies, roasted cuts of meat, grilled steak and chops and few   other cuisines of meat accompanied with vegetables. With the emphasis of   multiculturalism many other cuisines has influenced the Australian food   culture. They include Mediterranean and Asian foods formerly brought up   by the settlers. In the recent times, Australians consume a lot of   take-away food. </span></p>
                                            <p align="justify" class="style1"> The common breakfast in Australia include cereals, toast, fruits and   fried bacon, egg, mushroom, baked beans and sausages. Tea, coffee,   flavored milk or juice is the drink with breakfast. The common choices   in dinner are roast meat and vegetables, pasta, pizza, casseroles,   barbecue meat, vegetables and salads, soup, stir-fries. Try some good   Australian food so as to enjoy a pleasure of dining in Australian style.</p>
                                            <p align="justify"> <span class="style1"><strong>Multiculturalism</strong><br/>
                                            The people in Australia came from over 100 different countries around the world  with their own cultures, ethics and languages. They formed different cultural groups  that made Australia a multicultural nation, which was adopted since 1970. As there  are different cultural groups, Australian government implemented the cultural policy  in the country to control the society and its ethics. There is a multicultural national  literature in Australia. There are 26.4% of Catholics, 20.5% of Anglicans, 20.5%  of other Christians, 1.9% of Buddhists, 1.5% of Muslims, 1.2% of other religions,  12.7%, of unspecified religions in Australia. This has resulted students from different  countries with different cultures to choose Australia for their higher education. </span></p>
                                            <p align="justify"> <span class="style1"><strong>Job Market</strong><br/>
                                            The prediction of job opportunities for international students in Australia is associated  with the comprehensive visa requirements. In Australia there is good scope for various  professions including doctors, teachers, accountants, plumbers, nurses, carpenters,  dentists and IT managers. You can get the complete information on the applications  of visas migration on <a href="http://www.immi.gov.au/" target="_blank"> Department of Immigration and Citizenship</a> website. There are good opportunities  for the graduates who secure permanent residence in Australia. You can get further  details about graduate destinations on<a href="http://www.graduatecareers.com.au/" target="_blank"> Graduate Careers Australia</a>.</span></p>
                                            <p align="justify"> <span class="style1"><strong>Tourism</strong><br/>
  Tourism is the large sector of the economy in Australia, which was merged as a crucial  industry. For the years 2002, 2003, 2004 Australia has produced world&rsquo;s 10th  biggest revenue from the tourism. Every year about 4 billion tourists visit Australia  and Australians itself are the big domestic travelers. Australia combines an abundance  of landscapes. These comprise of deserts in the interior, hills, mountains, tropical  rain forests and densely populated coastal strips with long beaches. Distinct from  other continents, Australia has wide variety of unique plants and animal life. Find  more&ndash;<a href="http://www.australia.gov.au/Tourism_&amp;_Travel" target="_blank">Australian Tourism</a> </span></p>
