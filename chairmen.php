                                                        <br/>
                                                There is no doubt that 21st century will be the century of life  sciences. Many scientists and economic analysts therefore regard  Biotechnology as one of the key technologies for next few decades. It  is a fortunate and exciting coincidence that Biotechnology and  Bioinformatics are developing at an unprecedented rate. Now that the  genomic blue print of life has been almost completed, many more  opportunities will arise for the life sciences. Globalization has  brought about new opportunities, new markets and new education streams  fostering competition and international partnerships. This is an  opportunity we must seize. As Martin Luther King Jr. pointed out: &quot;We  must remember that intelligence is not enough. Intelligence plus  character -- that is the goal of true education.&quot;<br/>
                                                <br/>
                                                I feel confident in our ability to meet these and other challenges that  will be certain to appear. Our highly dedicated and committed team of  faculty and academicians are all set to achieve excellence in their  field of education. I am honored to be the leader of this team and look  forward to the opportunities and challenges ahead.<br/>
