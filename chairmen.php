                                              <p><strong>New Degrees of Learning </strong></p>
                                              <p>Founded  in 1992 and presently having a nationwide network of offices, Indo  American Studies evolved and flourished to take its place at the  forefront, as an innovative, academic pioneer. The task of leading this  marvelous institution into the 21st century is an enormous  responsibility, a challenge I eagerly, but solemnly, embrace. Our  mission is to assist and facilitate admissions to some of the best and  top ranked universities located globally. Every year we counsel several  thonusand students and guide them in securing admissions to Bachelors,  Masters and Doctoral programs and most of them with scholarships and  financial aid. Our alumni are proud of our service and routinely write  to us to let us know about their careers and accomplishments. IAS is an  intimate and harmonious intellectual community, where our counselors  work closely to serve the students, - in short, what a great  organization is supposed to do. There is perhaps no better testimony to  our success in that arena than the remarkable tradition of service to  scholastic & meritorious students in securing scholarships and  financial assistance from various schools. <br/>
                                                        <br/>
                                                        <strong>Transforming Education </strong><br/>
                                                        <br/>
                                                Many institutions of higher learning are remaking themselves to enhance  their relevance as knowledge organizations in an innovation-driven  global economy. At IAS our vision is to become a global knowledge  enterprise that builds synergies between education, research and  entrepreneurship. To attain this vision, we aim to inculcate a global  mindset and foster a spirit of entrepreneurship among our community of  professors, students and alumni.<br/>
                                                <br/>
                                                In a global environment where traditional competitive advantages have  shifted, companies face the pressure to reposition themselves,  restructure, outsource non-core operations or face imminent collapse.  Other companies ride on the opportunities thrown up by globalization,  by creating new industries and entering new markets. And when they do,  they scout for talents globally. The new reality for prospective  employees looking to make inroads into multi-national companies is  this: In the global marketplace, companies are looking for people with  overseas experience who can lead or work with a multicultural  workforce, and yet understand local operating conditions<br/>
                                                <br/>
                                                <strong>IAS Campus - With Tech Edge </strong><br/>
                                                <br/>
                                                We are in the process of setting up a world class institution of higher  learning incorporating state of the art technology. On completion, it  will be a center of academic excellence offering globally relevant and  technology driven programs. However, we have to build on our strengths  in education if we are to take a vibrant country like India upstream.  We are retaining the fundamental advantages of a competitive, national  system of education, while seeking to create new strengths. We are  creating a more flexible system and providing more choice to the  individual student, so as to encourage him or her to find their own  talents and interests and to develop them to the fullest. We intend to  offer industry-specific and globally relevant programs such as Business  Administration, Engineering and Computer Sciences at both Bachelors and  Masters Level.<br/>
                                                <br/>
                                                It is my profound belief, that as educators, we must do a better job of  cultivating the interests and aptitudes of our students in mathematics  and the sciences at the undergraduate and graduate levels. Indeed,  given the current global environment, every institution of higher  education must better prepare its students to face a host of cultural,  moral and ethical, intellectual and other challenges as we strive to  adapt to the new realities of our world. Higher education must instill  in students a set of values appropriate to our international community.  We must cultivate character, character built on a moral and spiritual  awareness of, and appreciation for, others -- a capacity for love and  respect for all people of all backgrounds. I believe, the function of  the university is not simply to teach bread winning, or to furnish  faculty to students or to be a center of polite society. It is, above  all, to be the organ of that fine adjustment between real life and the  growing knowledge of life, an adjustment which forms the secret of  civilization.<br/>
                                                <br/>
                                                The main campus is envisioned to have teaching facilities, lecture  halls, workshops and world-class accommodation along with centralized  services with Computer Services Unit, Educational and Staff Development  Unit, Digital Library, Student Advisory Services, Administrative  Offices etc. These facilities are supported by 'always-on' IT  infrastructure. Students will benefit from an extensive range of  sophisticated resources, which will be continually updated including: <br/>
                                                <br/>
                                                Well-equipped classrooms with state-of-the-art infrastructure  facilities such as Video-Conferencing, LCD monitors, Desktop Systems,  Broadband Internet connections etc. <br/>
                                                <br/>
                                                Computer laboratories with advanced software for word-processing,  spreadsheets, databases, CAD/CAM, mathematical applications and  programming languages. </p>
