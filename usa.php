                                          <td align="left" valign="top" class="style1"><div align="justify">Students looking for the best overseas education and planning to put  their careers on the fast track, USA is the hottest destination&quot;. This  is because it has immense range of specialties, high quality of education, great  opportunities, which attracts students from different countries of the world. Students can choose a suitable university from over 3800 universities and colleges  available in USA that facilitates several educational programs. U.S  provides a lot of offers for international students like, prestigious education  in the world, assorted cities and towns, dazzling natural parks, different events  and multicultural population.<br/>
                                            </div>
                                            <p align="justify"> To know more about USA Education <a href="https://educationusa.state.gov/">Click  Here</a></p>
                                            <p align="justify"> <strong>History</strong><br/>
                                              <br/>
                                              The United States of America is a constitutional federal republic nation on North  America continent consisting of fifty states and a federal district. After the fragmentation  of American&rsquo;s colonies with its mother country in 1776, USA was recognized  as a new nation. During the 19th and 20th centuries, 37 new states were added to  the original 13 states and it has expanded across the North America and other overseas  possessions.</p>
                                          <p align="justify"> The history of USA has two distressing experiences, Civil War (1861-65) and great  Depression (1930s). Upholding the victory of world wars I and II and the Cold War,  US has remained the powerful nation of the world. There was steady growth in the  US economy with low unemployment rate and inflation and has rapid advances in technology.</p></td>
                                        </tr>
