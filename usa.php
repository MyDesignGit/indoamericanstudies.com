                                          <td align="left" class="style1"><br/>
                                            <div align="justify"><strong><br/>
                                            Economy </strong> <br/>
                                            <br/>
                                            USA has the most powerful economy in the world both in terms of economy and technology  with a per capita GDP of $46000 in 2007 and with 12% below poverty line (as per <a href="https://www.cia.gov/library/publications/the-world-factbook/" target="_blank">CIA World Factbook</a>). The growth in GDP is strengthening by extensive  gains in labor productivity. Many companies in USA are ahead in technological advances,  specifically in computers, aerospace, medical, and in military equipments. <br/>
                                            <br/>
                                            Formerly, USA has fascinated several immigrants from all over the world and is still  the nation that has highly populated them compared to other industrialized countries.  All the gains in household income were constantly rising up by 20% of families.  Together, all these reasons explain the fiscal status of the country, which shows  good scope for the international students and build their careers.                                            </div>
                                            <p align="justify"> <strong>Climate </strong> <br/>
                                              <br/>
                                              As USA is a big country, its climatic condition varies from place to place. There  will be temperate climate in most part of the country. Usually the southern and  western half of the country will be warm than the other regions and extreme hot  in summer. The eastern and northern half region will be extremely cold and heavy  snowfall in winters and will be pleasant in summer. Places like Hawaii and Florida  will have tropical climate, freezing in places around Alaska and semi-dry in Great  Plains west regions of Mississippi River and dry in Great Basin of the southwest  region. Prior to their transition, overseas students have to prepare themselves  for the climatic conditions.<br/>
                                          <a href="https://www.cia.gov/library/publications/the-world-factbook/geos/us.html" target="_blank">Click Here </a>for more information about USA</p>
                                            <p align="justify"> <strong>Culture</strong><br/>
                                              <br/>
                                              USA is the country with diverse cultures and is the home for extensive groups of  ethics, customs and values. People in United States tend to the human values and  socioeconomic triumph. The people and culture in United States are much different  from Indians. The students who choose the universities will come across the regional  culture and rich traditions and customs. The nation has a multi-racial society that  merges new immigrants despite of the country from which they come from. The culture  of USA reveals a trend to hybridize pop culture, thus it is called as high culture.  The expansion of American culture was apparent in many aspects like, music, dance,  movies, sports, architecture, literature, poetry, cuisine and visual arts, which  are highly followed by all around the world.</p>
                                            <p align="justify"> Food snacks such as, fried chicken, pizzas, hamburgers, hot dogs, apple pie are  derived from the recipes of various immigrants. Many people even prefer to eat Italian  pastas, Mexican dishes such as burritos and tacos. Usually Americans prefer coffee  to tea and milk is the chronic breakfast drink. With the evolution of beverage industries,  orange juice and sweetened soft drinks are highly consumed.</p>
                                            <p align="justify"> <strong>Multiculturalism </strong><br/>
                                              <br/>
                                              In the United States, multiculturalism is the fundamental obsession expressing that  people from different culture, ethics and races are equal and they can keep their  identities, can take pride in their heritage and have a sense of belonging. It gives  a sense of safety and confidence for immigrants, making them more open to, and accepting  of, diverse cultures. Multiculturalism encourages racial and ethnic harmony and  cross-cultural understanding, and discourages ghettoization, hatred, discrimination  and violence. Multiculturalism enables United States to identify the prospective  of all Americans. It cheers people from other countries to incorporate into their  society and participate in its social, cultural, economic and political interactions. </p>
                                            <p align="justify"> <strong>Job Market</strong><br/>
                                              <br/>
                                              There is good job market for new graduates in United States. As per the reviews  of <a href="http://www.naceweb.org/" target="_blank">National Association  of Colleges and Employers (NACE)</a>, many companies in United States are competing  to hire new graduates. As a result there are increased opportunities for graduates  from abroad. According to the survey, around 60% of companies are planning to recruit  more graduates in 2007 than they recruited in the year 2006.</p>
                                            <p align="justify"> <strong>Food </strong><br/>
                                              <br/>
                                              Traditional American cuisine arts are much similar to several Western countries.  Wheat is the primary cereal grain. The main ingredients in American food include  turkey, potatoes, sweet potatoes, corn, slow-cooked pork and beef barbecue, crab  cakes, potato chips, and cookies. You can also find many Indian restaurants in every  city, where you can find unique cuisines from different regions of India.<a href="http://www.thokalath.com/restaurant/index.php" target="_blank"> Click Here </a>for the list of Indian restaurants  in USA. </p>
                                            <p align="justify"> According to <a href="http://www.bls.gov/" target="_blank">Bureau of Labor  Statistics </a>in June 2007, the unemployment rate in United States was decreased  to 4.5%. The report stated that the highest unemployment rate was for Michigan with  7.2% followed by Ohio with 6.1% and Mississippi with 6%, Idaho at 2.5% and Utah  at 2.6%. The lowest unemployment rate was for the states of Hawaii and Montana at  2.4% each. These facts states that there is good opportunities for the international  students in USA and for their careers on good path.</p>
                                            <p align="justify"> <strong>Tourism</strong><br/>
                                              <br/>
                                              There is a large tourism industry in U.S that provides millions of international  and domestic tourists. The country is the homeland for natural wonders, historical  landmarks, cities, and gambling spots. People in US seek for comparable attractions  in addition to the amusement and vacation areas. Many universities facilitate educational  tours for their students.</p>
