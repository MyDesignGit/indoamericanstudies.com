                                      <td colspan="2" valign="top" class="style1"><p align="justify"> New Zealand is located in the Southwest Pacific Ocean and consists of   two main Islands as well as a number of smaller ones. The principal   Islands are the North and South Islands which are separated by the Cook   Strait. </p>
                                        <p align="justify"> North Island has a mountainous center with many hot springs and volcanic   peaks. South Island is much more mountainous with the Southern Alps,   which has some 350 glaciers, running across the length of the Island.   Much of the lowlands are broken and hilly while plains are not prominent   on either Island. </p>
                                        <p align="justify"> <strong>History </strong><br/>
                                          It is estimated the Maori inhabited New Zealand around 800AD. Around   eight hundred years later the Western world discovered New Zealand. In   1642, in a bid to locate the mysterious, rich land Australia, the Dutch   Explorer, Abel Tasman caught sight of the West Coast of the South   Island. </p>
                                        <p align="justify"> The First European to set foot on New Zealand soil was Captain James   Cook of England, in 1769. He also made the first, but inaccurate map of   the country. </p>
                                        <p align="justify"> Settlers from England started to arrive in the 1830's, and by 1840 a   Treaty was signed between the crown and the chiefs of the Maori tribes.   The Treaty of Waitangi handed sovereignty of New Zealand to the Crown,   and is a matter of dispute even today, as the Maori translation is not   quite the same as the English. </p>
                                        <p align="justify"> One hundred and seven years later, in 1947 New Zealand declared independence, and became its own country. </p>
                                        <p align="justify"> <strong>Economy</strong><br/>
                                          New Zealand has a modern, prosperous, developed economy with an   estimated GDP (PPP) of US$119.549 billion (2010). New Zealanders have a   high level of life satisfaction as measured by international surveys.   The country was ranked 20th on the 2009 Human Development Index and 15th   in The Economist's 2005 worldwide quality-of-life index. </p>
                                        <p align="justify"> New Zealand is a country heavily dependent on free trade. Its principal   export industries are agriculture, horticulture, fishing and forestry.   These make up about half of the country's exports. Its major export   partners are Australia, US, Japan, China, and UK. Tourism plays a   significant role in New Zealand's economy. </p>
                                        <p align="justify"> <strong>Climate </strong><br/>
                                          New Zealand is in the southern hemisphere, so January and February are   the warmest months, autumn is from March to May, winter is from June to   August, and spring runs from September to November. </p>
                                        <p align="justify"> The climate is temperate with relatively mild, wet winters and warm, dry   summers. The weather varies a lot between different regions &ndash; the far   north is subtropical while the south gets icy wind straight from   Antarctica. The far north of the country has an average temperature of   about 15&deg;C, while the Deep South has a cooler 9&deg;C average. </p>
                                        <p align="justify"> New Zealand does not have a large temperature range, lacking the   extremes that are found in most continental climates. However, the   weather can change unexpectedly &mdash; as cold fronts or tropical cyclones   quickly blow in. </p>
                                        <p align="justify"> <strong>Culture</strong><br/>
                                          While New Zealand is culturally and linguistically part of Polynesia,   forming the south-western anchor of the Polynesian Triangle, much of   contemporary New Zealand culture is derived from British roots. It also   includes significant influences from American, Australian and M&#257;ori   cultures, along with those of other European cultures and &ndash; more   recently &ndash; non-M&#257;ori Polynesian and Asian cultures. </p>
                                        <p align="justify"> Celebration of Diwali and Chinese New Year are held in several of the   larger cities. The world's largest Polynesian festival, Pasifika, is an   annual event in Auckland. </p>
                                        <p align="justify"> <strong>Food </strong><br/>
                                          New Zealand cuisine is largely driven by local ingredients and seasonal   variations. Occupying an Island nation with a primarily agricultural   economy, New Zealanders enjoy quality local produce from land and sea.   Similar to the cuisine of Australia, the cuisine of New Zealand is a   diverse British-based cuisine with Mediterranean and Pacific Rim   influences. </p>
                                        <p align="justify"> Historical influences came from M&#257;ori culture, and New American cuisine,   Southeast Asian, East Asian and Indian traditions have become popular   since the 1970s. </p>
                                        <p align="justify"> <strong>Public Holidays </strong><br/>
                                        </p>
                                        <div align="justify">
                                          <ol>
                                            <li> New Year's Day - January 1st </li>
                                            <li> Day after New Year &ndash; January 2nd</li>
                                            <li> Waitangi Day &ndash; February 6th</li>
                                            <li> Good Friday &ndash; varies every year</li>
                                            <li> Easter Monday &ndash; varies every year</li>
                                            <li> ANZAC Day &ndash; April 25th</li>
                                            <li> Queen&rsquo;s Birthday &ndash; 1st Monday in June</li>
                                            <li> Labour Day &ndash; 4th Monday in October</li>
                                            <li> Christmas Day &ndash; December 25th</li>
                                            <li> Boxing Day &ndash; December 26th </li>
                                          </ol>
                                        </div>
                                        <p align="justify"> <strong>Time Zones </strong><br/>
                                          New Zealand has two time zones. </p>
                                        <div align="justify">
                                          <ol>
                                            <li> The main Islands use New Zealand Standard Time (NZST) &ndash; 6:30 hours plus IST                                            </li>
                                            <li> Chatham Islands use Chatham Standard Time (CHAST) &ndash; 7:15 hours plus IS</li>
                                          </ol>
