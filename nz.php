                                          Area: 268,021 sq km<br/>
                                          Official languages: English, M&#257;ori<br/>
                                          Major religion: Christianity <br/>
                                          Life expectancy: 76 years (men), 81 years (women)<br/>
                                          Monetary unit: 1 NZ dollar ($NZ) = 100 cents <br/>
                                          Main exports: Wool, food and dairy products, wood and paper products<br/>
                                          GNI per capita: $25,200 (World Bank, 2008) <br/>
                                          Internet domain: .nx<br/>
                                      International dialing code: +64 </div>                                        </td>
